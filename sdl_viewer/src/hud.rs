pub struct HudRenderer {
    gl: Rc<opengl::Gl>,
    program: u32,
    vao: u32,
    vbo: u32,
    u_screen: i32,
    u_color: i32,
}

impl HudRenderer {
    pub fn new(gl: &Rc<opengl::Gl>) -> Self {
        let gl = Rc::clone(gl);

        let vert_src = "#version 410 core\n\
            layout(location = 0) in vec2 pos;\n\
            uniform vec2 screen;\n\
            void main() {\n\
                gl_Position = vec4((pos.x/screen.x)*2.0-1.0, 1.0-(pos.y/screen.y)*2.0, 0.0, 1.0);\n\
                gl_PointSize = 2.0;\n\
            }";
        let frag_src = "#version 410 core\n\
            out vec4 c;\n\
            uniform vec4 color;\n\
            void main() { c = color; }";

        let program = unsafe {
            let v = compile_shader(&gl, vert_src, opengl::VERTEX_SHADER);
            let f = compile_shader(&gl, frag_src, opengl::FRAGMENT_SHADER);
            link_program(&gl, v, f)
        };
        let u_screen = unsafe { gl.GetUniformLocation(program, b"screen\0".as_ptr() as *const i8) };
        let u_color  = unsafe { gl.GetUniformLocation(program, b"color\0".as_ptr()  as *const i8) };

        let (mut vao, mut vbo) = (0u32, 0u32);
        unsafe {
            gl.GenVertexArrays(1, &mut vao);
            gl.GenBuffers(1, &mut vbo);
            gl.BindVertexArray(vao);
            gl.BindBuffer(opengl::ARRAY_BUFFER, vbo);
            gl.EnableVertexAttribArray(0);
            gl.VertexAttribPointer(0, 2, opengl::FLOAT, 0u8, 8, std::ptr::null());
            gl.BindVertexArray(0);
        }

        HudRenderer { gl, program, vao, vbo, u_screen, u_color }
    }

    pub fn draw(&self, width: i32, height: i32, lines: &[String]) {
        const SCALE: f32 = 2.0;
        const CW: f32 = 8.0 * SCALE;
        const CH: f32 = 8.0 * SCALE;
        const LINE_GAP: f32 = 4.0;
        const MARGIN: f32 = 8.0;
        const PAD: f32 = 6.0;

        let max_chars = lines.iter().map(|l| l.chars().count()).max().unwrap_or(0);
        let box_w = max_chars as f32 * CW + MARGIN * 2.0;
        let box_h = lines.len() as f32 * (CH + LINE_GAP) - LINE_GAP + MARGIN * 2.0;
        let bx1 = width as f32 - PAD;
        let by1 = height as f32 - PAD;
        let bx0 = bx1 - box_w;
        let by0 = by1 - box_h;

        let mut pts: Vec<f32> = Vec::with_capacity(4096);
        for (li, line) in lines.iter().enumerate() {
            let y0 = by0 + MARGIN + li as f32 * (CH + LINE_GAP);
            for (ci, ch) in line.chars().enumerate() {
                let x0 = bx0 + MARGIN + ci as f32 * CW;
                let code = ch as usize;
                if code < 32 || code > 127 { continue; }
                for (row, &byte) in FONT[code - 32].iter().enumerate() {
                    for col in 0u32..8 {
                        if byte & (1u8 << col) != 0 {
                            pts.push(x0 + col as f32 * SCALE);
                            pts.push(y0 + row as f32 * SCALE);
                        }
                    }
                }
            }
        }

        let gl = &self.gl;
        unsafe {
            gl.Disable(opengl::DEPTH_TEST);
            gl.Enable(opengl::BLEND);
            gl.BlendFunc(opengl::SRC_ALPHA, opengl::ONE_MINUS_SRC_ALPHA);
            gl.Enable(opengl::PROGRAM_POINT_SIZE);

            gl.UseProgram(self.program);
            gl.Uniform2f(self.u_screen, width as f32, height as f32);
            gl.BindVertexArray(self.vao);
            gl.BindBuffer(opengl::ARRAY_BUFFER, self.vbo);

            // background
            let bg: [f32; 12] = [bx0,by0, bx1,by0, bx1,by1, bx0,by0, bx1,by1, bx0,by1];
            gl.BufferData(opengl::ARRAY_BUFFER, (bg.len() * 4) as isize,
                bg.as_ptr() as *const _, opengl::DYNAMIC_DRAW);
            gl.Uniform4f(self.u_color, 0.0, 0.0, 0.0, 0.65);
            gl.DrawArrays(opengl::TRIANGLES, 0, 6);

            // text glyphs
            if !pts.is_empty() {
                gl.BufferData(opengl::ARRAY_BUFFER, (pts.len() * 4) as isize,
                    pts.as_ptr() as *const _, opengl::DYNAMIC_DRAW);
                gl.Uniform4f(self.u_color, 1.0, 1.0, 0.2, 1.0);
                gl.DrawArrays(opengl::POINTS, 0, (pts.len() / 2) as i32);
            }

            gl.BindVertexArray(0);
            gl.Disable(opengl::BLEND);
            gl.Disable(opengl::PROGRAM_POINT_SIZE);
            gl.Enable(opengl::DEPTH_TEST);
        }
    }
}

impl Drop for HudRenderer {
    fn drop(&mut self) {
        unsafe {
            self.gl.DeleteProgram(self.program);
            self.gl.DeleteVertexArrays(1, &self.vao);
            self.gl.DeleteBuffers(1, &self.vbo);
        }
    }
}

unsafe fn compile_shader(gl: &opengl::Gl, src: &str, kind: u32) -> u32 {
    let shader = gl.CreateShader(kind);
    let cs = CString::new(src).unwrap();
    let ptr = cs.as_ptr();
    gl.ShaderSource(shader, 1, &ptr, std::ptr::null());
    gl.CompileShader(shader);
    let mut ok = 0i32;
    gl.GetShaderiv(shader, opengl::COMPILE_STATUS, &mut ok);
    if ok == 0 {
        let mut len = 0i32;
        gl.GetShaderiv(shader, opengl::INFO_LOG_LENGTH, &mut len);
        let mut buf = vec![0u8; len as usize];
        gl.GetShaderInfoLog(shader, len, std::ptr::null_mut(), buf.as_mut_ptr() as *mut i8);
        panic!("HUD shader error: {}", String::from_utf8_lossy(&buf));
    }
    shader
}

unsafe fn link_program(gl: &opengl::Gl, vert: u32, frag: u32) -> u32 {
    let prog = gl.CreateProgram();
    gl.AttachShader(prog, vert);
    gl.AttachShader(prog, frag);
    gl.LinkProgram(prog);
    let mut ok = 0i32;
    gl.GetProgramiv(prog, opengl::LINK_STATUS, &mut ok);
    if ok == 0 {
        let mut len = 0i32;
        gl.GetProgramiv(prog, opengl::INFO_LOG_LENGTH, &mut len);
        let mut buf = vec![0u8; len as usize];
        gl.GetProgramInfoLog(prog, len, std::ptr::null_mut(), buf.as_mut_ptr() as *mut i8);
        panic!("HUD link error: {}", String::from_utf8_lossy(&buf));
    }
    gl.DeleteShader(vert);
    gl.DeleteShader(frag);
    prog
}
